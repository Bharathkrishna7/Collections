package Simplejavapro;

public class Datatypes { // class name 1st letter always starts with Capital letter
   public static void main(String[] args) {
	/*data types
	   Here int,char,double,boolean are "primitive data types"	
	   */
	  
	    int i=6;
	   i=11; // here it is replacing the i value into 11 from 6 . so duplicate "rv" is allowed 
		char m='$';
		char q='k';
		boolean b=true;
		double o=26.9;
	System.out.println(b);
	System.out.println(i&m);
	System.out.println(q+o);
		
	// String is a class not a data type. & we can write any kind of data type values..
		 String s="radha";
		 String d="28.0";
		 String f="n";
		 String g="7";
		 System.out.println(f);
		System.out.println(s+d+g);
		

	}

}
